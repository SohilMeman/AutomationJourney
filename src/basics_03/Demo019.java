package src.basics_03;

public class Demo019 {
    public static void main(String[] args) {
        //Logical Operator
        // !, &, or like AND, OR , NOT gates
        // ! -> Not
        boolean a = true;
        System.out.println(a);
        System.out.println(!a);
        System.out.println(10>20);
        System.out.println(!(10>20));
        System.out.println(!(30>90));
    }
}
