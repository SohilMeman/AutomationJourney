package src.basics_03;

public class Demo020 {
    public static void main(String[] args) {

        //Logical Operator
        // || Or gate

        System.out.println(true || true);
        System.out.println(true || false);
        System.out.println(false || true);
        System.out.println(false || false);

        System.out.println("--------");

        // &  AND gate

        System.out.println(true && true);
        System.out.println(true && false);
        System.out.println(false && true);
        System.out.println(false && false);
    }
}
