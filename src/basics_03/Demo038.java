package src.basics_03;

public class Demo038 {
    public static void main(String[] args) {
        int a = 32;

        if (a%2 ==1){
            System.out.println("Odd Number");
        }else{
            System.out.println("Even Number");
        }

        int b = 124;

        if (b%2 ==0){
            System.out.println("Odd Number");
        }else {
            System.out.println("Even Number");
        }

        int c = 55;

        if (c%2 ==0){
            System.out.println("I am printed because condition becomes true");
        }else {
            System.out.println("I am printed because condition becomes false");
        }
    }
}
