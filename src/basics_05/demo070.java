package src.basics_05;

public class demo070 {
    public static void main(String[] args) {

        // While and Do While
        //

        int i = 1; // A - Init
        while(i<=10){ // B - Cond
            System.out.println(i); // D - Code Execute
            i++; // C - I/D
        }

        for (int j = 1; j <=10 ; j++) {
            System.out.println(j);
            }
        }
    }

